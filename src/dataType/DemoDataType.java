package dataType;

public class DemoDataType {

	public static void main (String arg[]) {
	
		System.out.println("Hello world");
		
		int n1= 10;
		int n2= 20;
		System.out.println(n1+n2);
		
		//Below eight digit numbers........we need to use (f) after value......
		float f1= 12.5f;
		float f2=13.5f;
		System.out.println(f1-f2);
		
		//above eight digit numbers in point....
		double d1=12345.4567;
		double d2=54321.09543;
		System.out.println(d1-d2);
		
		//above eight digit numbers..... we can use (L) are not our wish....... 
		long l1=40;
		long l2=5;
		System.out.println(l1/l2);
	}
}
