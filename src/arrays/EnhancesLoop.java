package arrays;

public class EnhancesLoop {
	
	public static void main(String args[]) {
		
		// enhanced for is end to end print 
		
		int nums[]= {1,2,3,4};
		
		for(int n:nums) {
			System.out.println(n);
		}
		
		System.out.println(".............String.............");
		
		String names[]= {"bhuvi","lolita","Bhuvanes","lalith"};
		
		for(String n:names) {
			System.out.println(n);
		}
	}

}
